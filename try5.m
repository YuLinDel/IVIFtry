%区间直觉模糊数排序
%
%输入
%输出
%

clear 
clc

%输入历史值
A=[0.226881615792136,0.341854141458266,0.538794953629984,0.648059407291143;
    0.531635220223983,0.637919850818771,0.217217978943122,0.319112187227530;
    0.505523267634915,0.606333040125142,0.238279276766151,0.338521593021065;
    0.303100996671597,0.414431541320559,0.360207110789499,0.463258061195939;
    0.524649150587868,0.628807798661578,0.214456872406501,0.299904846447343];
B=[0,0.219832516454287,0.455681063471099,0.575920839820106;
    0.459658225056772,0.563312236900285,0.182190412629554,0.285138764221969;
    0.425329334433559,0.526856695106964,0.160614788326661,0.261172876722832;
    0.246211837727800,0.366468868294696,0.289350179778936,0.393646042797359;
    0.451071470665968,0.577267480787850,0.120934088243659,0.221468004644300];
C=[0,0.223961485228046,0.662437892500327,0.746972010584423;
    0.468832823227376,0.547320042987025,0.350723091661606,0.452679957012975;
    0.427399911832517,0.502538706670540,0.363789363955216,0.442518475367577;
    0.253514933044890,0.374532309093678,0.449592091552836,0.539442023543655;
    0.418235866109863,0.524677011383473,0.266291828574554,0.371413296492401];

%加入时间权重

wt=[0.238,0.323,0.438];


DD=[A B C];
R=mat2cell(DD,[1,1,1,1,1],[4,4,4]);%直接存成了cell型
A=5;
G=3;
for l=1:A
D=zeros(G,G);
a1=0;a2=0;b1=0;b2=0;c1=0;c2=0;d1=0;d2=0;
for i=1:G
    m=R{l,i};
    a1=m(1);
    b1=m(2);
    c1=m(3);
    d1=m(4);
    for j=1:G
        if j==i
            continue
        end
        n=R{l,j};
        a2=n(1);
        b2=n(2);
        c2=n(3);
        d2=n(4);
        D(i,j)=0.25*(abs(a1-a2)+abs(b1-b2)+abs(c1-c2)+abs(d1-d2));
    end
end

%%求静态集结值
sum_sup=sum(D,2)';
T=zeros(G,G);
sum_e=zeros(1,G);
zs=zeros(1,G);
for i=1:G
    for j=1:G
        if j==i
            continue
        end
        T(i,j)=wt(j)*(1-(D(i,j)/sum_sup(i)));
        T_a=sum(T,2)';
        sum_e(i)=wt(i)*(1+T_a(i));
        sum_zs=sum(sum_e);
    end
%     zs(i)=sum_e(i)/sum_zs;
end
% IIFPWGA=zeros(A,G);
a_e=zeros(1,G);
b_e=zeros(1,G);
c_e=zeros(1,G);
d_e=zeros(1,G);
IA1=1;
IA2=1;
IA3=1;
IA4=1;
for i=1:G
    zs(i)=sum_e(i)/sum_zs;
    a_e(i)=DD(l,(i-1)*4+1)^zs(i);
    IA1=IA1*a_e(i);
    b_e(i)=DD(l,(i-1)*4+2)^zs(i);
    IA2=IA2*b_e(i);
    c_e(i)=(1-DD(l,(i-1)*4+3))^zs(i);
    IA3=IA3*c_e(i);
    d_e(i)=(1-DD(l,(i-1)*4+4))^zs(i);
    IA4=IA4*d_e(i);
end
IIFPWGA(l,:)={IA1,IA2,(1-IA3),(1-IA4)};
end

IIFP=cell2mat(IIFPWGA);
for i=1:A
    S(i)=(IIFP(i,1)+IIFP(i,2)-IIFP(i,3)-IIFP(i,4))/2;
    H(i)=(IIFP(i,1)+IIFP(i,2)+IIFP(i,3)+IIFP(i,4))/2;
end


